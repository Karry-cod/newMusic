<script lang="ts">
import { defineComponent, onMounted, reactive, toRefs } from "vue"
import { useRouter } from "vue-router"
import MV from "@/api/apis/mv"
import MusicForm from "@/api/apis/musicForm"
import type { IMV } from "@/utils/interfaces"
import { ElMessage } from "element-plus"
import router from "@/router"

export default defineComponent({
    setup: () => {
        let data = reactive({
            mv: <IMV>{},
            comment: {
                myComment: "",
                hotComment: <any>[],
                newComment: <any>[]
            },
            recommendMVs: <IMV[]>{},
            delta: 0
        });

        let methods = {
            // 发表评论
            sendComment(t: number) {
                MusicForm.sendComment({ t: t, type: 1, id: data.mv.mvid, content: data.comment.myComment, timestamp: new Date().getTime() }).then((res: any) => {
                    if (res.code === 200) {
                        ElMessage.success("竟然成功了！！！");
                    } else {
                        ElMessage.error("接口尚有故障，暂时关闭评论功能");
                    }
                });
            },
            // 时间格式补零【mm:ss】
            repairZero(num: number) {
                if (num < 10) {
                    return "0" + num;
                } else {
                    return num.toString();
                }
            },
            // 视频暂停
            videoPause() {
                let videos = document.querySelectorAll("video");
                videos.forEach((d: any) => {
                    d.pause();
                });
            },
            // 视频播放
            videoPlay() {
                let videos = document.querySelectorAll("video");
                videos.forEach((d: any) => {
                    d.play();
                });
            }
        };

        let events = {
            // 获取mv播放地址
            getMVUrl() {
                MV.getMVUrl({ id: data.mv.mvid }).then((res: any) => {
                    if (res.code === 200) {
                        data.mv.source = res.data.url;
                        events.getMVComment();
                        events.watchScrollTop();
                    } else {
                        events.getVideoDetail();
                        events.getVideoUrl();
                        events.getVideoComment();
                        events.watchScrollTop();
                    }
                });
            },
            // 获取MV评论
            getMVComment() {
                MV.getMVComment({ id: data.mv.mvid }).then((res: any) => {
                    if (res.code === 200) {
                        data.comment.hotComment = res.hotComments;
                        data.comment.newComment = res.comments;
                    }
                });
            },
            // 获取相关推荐的MV
            getRecommendMV() {
                MV.getRecommendMV({ id: data.mv.mvid }).then(res => {
                    if (res.code === 200) {
                        data.recommendMVs = res.data.map((d: any) => {
                            let time = (d.durationms / 60000).toFixed(2);
                            let minute = time.split(".")[0];
                            let second = time.split(".")[1];
                            let playTime = methods.repairZero(parseInt(minute)) + ":" + methods.repairZero(parseInt(((parseInt(second)) / 100 * 60).toString()));
                            return <IMV>{
                                mvid: d.vid,
                                listener: d.playTime,
                                time: d.durationms,
                                author: d.creator[0].userName,
                                backImg: d.coverUrl,
                                name: d.title,
                                playTime,
                                createrId: d.creator[0].userId
                            };
                        });
                    }
                });
            },
            // 获取视频详情
            getVideoDetail() {
                MV.getVideoDetail({ id: data.mv.mvid }).then(res => {
                    console.log(res, "视频详情");
                    if (res.code === 200) {

                    }
                });
            },
            // 获取视频地址
            getVideoUrl() {
                MV.getVideoUrl({ id: data.mv.mvid }).then((res: any) => {
                    if (res.code === 200) {
                        data.mv.source = res.urls[0].url;
                    }
                });
            },
            // 获取视频评论
            getVideoComment() {
                MV.getVideoComment({ id: data.mv.mvid }).then((res: any) => {
                    if (res.code === 200) {
                        data.comment.hotComment = res.hotComments;
                        data.comment.newComment = res.comments;
                    }
                });
            },
            // 监听页面的滚动
            watchScrollTop() {
                let body = document.querySelectorAll(".main")[0] as HTMLElement;
                let mvOutside = document.querySelectorAll(".mv_outside")[0] as HTMLElement;
                let comment = document.querySelectorAll(".comment_Outside")[0] as HTMLElement;
                body.addEventListener("mousewheel", (e: any) => {
                    e = e || window.event;
                    data.delta += e.wheelDelta;
                    let height = mvOutside.clientHeight + comment.clientHeight;
                    if (data.delta > 0) {
                        data.delta = 0;
                    } else if (data.delta * -1 > height - 1000) {
                        data.delta -= e.wheelDelta;
                    }
                    if (data.delta <= -600) {
                        (document.querySelectorAll(".rb_video")[0] as HTMLElement).style.display = "block";
                    } else {
                        (document.querySelectorAll(".rb_video")[0] as HTMLElement).style.display = "none";
                    }
                });
            }
        };

        onMounted(() => {
            let router = useRouter();
            let mid = router.currentRoute.value.params.mid;
            data.mv.mvid = (mid as string);
            events.getMVUrl();
            events.getRecommendMV();
        });

        return {
            ...toRefs(data),
            ...methods,
            router
        };
    }
})
